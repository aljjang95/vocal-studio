/**
 * 사용자 질문에서 FAQ 매칭을 시도한다.
 * @returns 매칭된 답변 또는 null (매칭 실패 시 API 호출 필요)
 */
export function matchFaq(userMessage: string): FaqEntry | null {
  const msg = userMessage.toLowerCase().replace(/\s+/g, ' ').trim();
  if (msg.length < 3) return null;

  let bestMatch: FaqEntry | null = null;
  let bestScore = 0;

  for (const entry of faqDatabase) {
    let matchCount = 0;
    for (const kw of entry.keywords) {
      if (msg.includes(kw.toLowerCase())) {
        matchCount++;
      }
    }
    if (matchCount >= entry.minMatch && matchCount > bestScore) {
      bestScore = matchCount;
      bestMatch = entry;
    }
  }

  return bestMatch;
}
